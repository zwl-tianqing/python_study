# 定义整数 int 标识整数类型
a = 1
print(type(a))

# 定义浮点数 float 表示浮点数类型
b = 1.1
print(type(b))

# 定义布尔值类型 bool 表示布尔值类型
# 布尔值只有两个数据 Ture False
c = True or False
print(type(c))

# 定义字符串 str 表示字符串类型 字符串数据需要用引号括起来
d = 'hello'
print(type(d))

# 定义列表 list 表示列表
e = [1, 3, 4, 5]
print(type(e))

# 定义元组 tuple 表示元组
n = (1, 3, 5)
print(type(n))


# 定义集合 set 表示集合
f = {1, 3, 5, 2}
print(type(f))

# 定义字典
g = {'name': 'tom', 'age': '18'}
print(type(g))
